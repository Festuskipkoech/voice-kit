#  silence tests 
@pytest.mark.asyncio
async def test_silence_produces_no_transcript(pipeline):
    """
    Pure silence sent to Whisper must return empty transcript.
    Whisper's no_speech_threshold=0.6 suppresses hallucinations.
    This is critical — hallucinated responses waste LLM tokens
    and produce unsolicited agent responses.
    """
    audio_in = await make_silence_queue(duration_seconds=1.0)
    audio_out: asyncio.Queue = asyncio.Queue()

    metrics = await pipeline.run_turn(audio_in, audio_out)

    assert metrics.transcript == "", (
        f"Whisper hallucinated on silence: '{metrics.transcript}'. "
        f"Increase no_speech_threshold in WhisperSTT.transcribe() "
        f"or set log_prob_threshold to a less negative value."
    )
    assert audio_out.empty(), (
        "Silence produced audio output — pipeline should stop "
        "when transcript is empty."
    )


