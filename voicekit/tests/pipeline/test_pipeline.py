"""
Pipeline tests.

Tests the full VoicePipeline with real providers — Whisper STT,
Chatterbox TTS, Claude LLM, and Silero VAD.

Requirements:
    - ANTHROPIC_API_KEY set in environment
    - faster-whisper installed
    - chatterbox-tts installed
    - silero-vad installed
    - tests/fixtures/speech.raw must exist (generated by setup script below)

Generate speech fixture:
    mkdir -p tests/fixtures
    python -c "
    from gtts import gTTS
    import subprocess
    tts = gTTS('hello how are you doing today', lang='en')
    tts.save('/tmp/speech.mp3')
    subprocess.run(['ffmpeg', '-i', '/tmp/speech.mp3',
        '-ar', '16000', '-ac', '1', '-f', 'f32le',
        'tests/fixtures/speech.raw'], check=True)
    "

VAD note:
    Pipeline tests disable VAD (vad.enabled=False).
    VAD correctness is fully covered in tests/unit/test_vad.py.
    Pipeline tests focus on pipeline orchestration — STT, LLM, TTS
    working together correctly, not on VAD filtering behaviour.

Silence note:
    Whisper hallucination on silence ('Thank you.', 'you', etc) is
    suppressed via no_speech_threshold=0.6 in WhisperSTT.transcribe().
    Pure silence with those settings returns empty transcript.
"""
import asyncio
import os
import pathlib
import time

import numpy as np
import pytest
# add at the very top after imports
from dotenv import load_dotenv
load_dotenv()   # loads .env from current directory automatically
from voicekit.config import VoiceConfig, STTConfig, TTSConfig, VADConfig, LLMConfig
from voicekit.pipeline import VoicePipeline

SAMPLE_RATE = 16000
CHUNK_SAMPLES = 1600   # 100ms at 16kHz

# module-level shared pipeline — models load once for all tests
_shared_pipeline = None

def make_config() -> VoiceConfig:
    return VoiceConfig(
        project="test",
        stt=STTConfig(model="whisper", variant="tiny"),
        tts=TTSConfig(model="chatterbox-turbo", voice="default"),
        vad=VADConfig(
            enabled=False,    # disabled — VAD tested in test_vad.py
            sensitivity=0.5
        ),
        llm=LLMConfig(
            provider="anthropic",
            model="claude-haiku-4-5",
            api_key=os.environ.get("ANTHROPIC_API_KEY", ""),
        ),
        system_prompt=(
            "You are a helpful assistant. "
            "Reply in one short sentence only."
        ),
    )

@pytest.fixture
async def pipeline():
    """
    Load pipeline once for all tests via module-level cache.
    Whisper tiny + Chatterbox take ~30s on first load.
    Subsequent tests reuse the same loaded instance.
    VAD state is reset before each test.
    """
    global _shared_pipeline
    if _shared_pipeline is None:
        p = VoicePipeline(make_config())
        await p.load()
        _shared_pipeline = p

    # reset VAD state before each test even though VAD is disabled
    # keeps the instance clean between tests
    _shared_pipeline.vad.reset_states()

    return _shared_pipeline

async def make_silence_queue(duration_seconds: float = 1.0) -> asyncio.Queue:
    """Queue of pure silence chunks."""
    queue: asyncio.Queue = asyncio.Queue()
    num_chunks = int(duration_seconds * SAMPLE_RATE / CHUNK_SAMPLES)
    for _ in range(num_chunks):
        await queue.put(np.zeros(CHUNK_SAMPLES, dtype=np.float32))
    return queue

async def make_real_audio_queue() -> asyncio.Queue:
    """
    Load real speech audio from fixture file.
    Fixture generated by gTTS + ffmpeg — actual human speech converted
    to 16kHz float32 PCM. Whisper transcribes this accurately.
    Falls back to silence if fixture not found.
    """
    fixture = pathlib.Path("tests/fixtures/speech.raw")
    queue: asyncio.Queue = asyncio.Queue()

    if fixture.exists():
        audio = np.frombuffer(fixture.read_bytes(), dtype=np.float32)
    else:
        pytest.skip(
            "tests/fixtures/speech.raw not found. "
            "Generate it with: "
            "mkdir -p tests/fixtures && "
            "python -c \"from gtts import gTTS; import subprocess; "
            "tts = gTTS('hello how are you doing today', lang='en'); "
            "tts.save('/tmp/speech.mp3'); "
            "subprocess.run(['ffmpeg', '-i', '/tmp/speech.mp3', "
            "'-ar', '16000', '-ac', '1', '-f', 'f32le', "
            "'tests/fixtures/speech.raw'], check=True)\""
        )

    for i in range(0, len(audio), CHUNK_SAMPLES):
        chunk = audio[i:i + CHUNK_SAMPLES]
        if len(chunk) < CHUNK_SAMPLES:
            chunk = np.pad(chunk, (0, CHUNK_SAMPLES - len(chunk)))
        await queue.put(chunk)

    return queue


